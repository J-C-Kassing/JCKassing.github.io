document.addEventListener("DOMContentLoaded", function() {
  // Get avatar element
  const avatar = document.getElementById('user-avatar'); // change if your img/div has a different ID

  if (!avatar) return;

  // Define hover images in order of your nav buttons
  const hoverImages = [
    '/images/Kassing_close 2.jpg',  // btn-1
    '/images/Kassing_close 2.jpg',  // btn-2
    '/images/Kassing_paper.png',    // btn-3
    '/images/Kassing_talk.png',  // btn-4
    '/images/Kassing_teaching.png', // btn-5
    '/images/Kassing_close 2.jpg',  // btn-6
  ];

  // Preload all hover images
  hoverImages.forEach(src => {
    const img = new Image();
    img.src = src;
  });

  // Select all nav items generated by your for loop
  const navItems = document.querySelectorAll('.masthead__menu-item');

  navItems.forEach((btn, index) => {
    btn.addEventListener('mouseenter', () => {
      if (avatar.tagName.toLowerCase() === 'img') {
        avatar.src = hoverImages[index];
      } else {
        avatar.style.backgroundImage = `url('${hoverImages[index]}')`;
      }
    });

    btn.addEventListener('mouseleave', () => {
      if (avatar.tagName.toLowerCase() === 'img') {
        avatar.src = '/images/Kassing_close.jpg'; // default avatar
      } else {
        avatar.style.backgroundImage = `url('/images/Kassing_close.jpg')`;
      }
    });
  });
});
